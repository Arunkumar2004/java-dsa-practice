package com.arun;

// do while loop is for it gives the output at least once if the condition fail also.

public class DoWhile {
    public static void main(String[] args) {

        /* syntax of do while:
            do {
                // body
            } while (condition);

        }
         */

        int n = 1;
        do {
            System.out.println(n);
        } while (n != 1);
    }
}
